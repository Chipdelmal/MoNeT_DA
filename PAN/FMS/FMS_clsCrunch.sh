#!/bin/bash

USR=$1
DRV=$2
QNT=$3
AOI=$4

###############################################################################
# Setup Path
###############################################################################
parent_path=$( cd "$(dirname "${BASH_SOURCE[0]}")" ; pwd -P )
cd "$parent_path"
###############################################################################
# Launch Scripts
###############################################################################
for mtr in "WOP" "CPT" "TTI" "TTO" "POE"
do
    python FMS_clsCompile.py $USR $DRV $QNT $AOI $mtr
done